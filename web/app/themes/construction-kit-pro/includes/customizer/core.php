if ( ! function_exists( 'construction_kit_reset_fonts' ) ) :

    function construction_kit_reset_fonts( $data ) {

            $reset_fonts = construction_kit_get_option( 'reset_fonts' );

            if ( true == $reset_fonts ) {

                $options = construction_kit_get_options();

                $options['theme_options']['reset_fonts'] = false;

                $font_settings = construction_kit_get_default_font_options();

                if ( ! empty( $font_settings ) ) {

                    foreach ( $font_settings as $key => $val ) {

                        $options['theme_options'][ $key ] = $val;

                    }
                }

                update_option( 'theme_mods_construction-kit-pro', $options );

            }

    }

endif;

add_action( 'customize_save_after', 'construction_kit_reset_fonts' );